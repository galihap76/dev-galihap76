var jmlAngkot=5;
var noAngkot=1;

while(noAngkot<=jmlAngkot){
	
	console.log('No Angkot. ' + noAngkot + ' Beroperasi Dengan Baik.');
	
noAngkot++;
	
}

//bagian kedua//
var jmlData=5;
var noData=1;

for(noData=1; noData<=jmlData; noData++){
	
	console.log('No Data. ' + noData + ' Beroperasi Dengan Baik.');
}

//bagian ketiga//
var jmlRemote=10;
var remoteBerjalan=6;
var noRemote=1;

while(noRemote<=remoteBerjalan){
	
	console.log('No Remote. ' + noRemote + ' Berjalan Dengan Baik.');
	
noRemote++;
	
}

for(noRemote<=remoteBerjalan +1; noRemote<=jmlRemote; noRemote++){
	
	console.log('No Remote. ' + noRemote + ' Tidak Berjalan Dengan Baik.');
}















